    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        //filter the files
        filter = new FileNameExtensionFilter("*.Images", "jpg", "png");
        file.addChoosableFileFilter(filter);
        result = file.showSaveDialog(null);
        //if the user click on save in Jfilechooser
        if (result == JFileChooser.APPROVE_OPTION) {
            selectedFile = file.getSelectedFile();
            path = selectedFile.getAbsolutePath();
//               JOptionPane.showMessageDialog(this,path);
            jLabel3.setIcon(new ImageIcon(new ImageIcon(path).getImage()
                    .getScaledInstance(jLabel3.getWidth(), jLabel3.getHeight(), Image.SCALE_SMOOTH)));
            choosed = true;

        }
//else if (result == JFileChooser.CANCEL_OPTION) {
//            JOptionPane.showMessageDialog(this, "No Image Selected");
//            path = "";
//
//        }
    }//GEN-LAST:event_jLabel3MouseClicked

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        for (Auteur auteur : aut) {
            if (tablemodel.getValueAt(jTable1.getSelectedRow(), 0).equals(auteur.getIdAut())) {
                jTextField1.setText(auteur.getNom());
                jDateChooser1.setDate(auteur.getDateN());
                jLabel3.setIcon(new ImageIcon(new ImageIcon(auteur.getPhoto()).getImage()
                        .getScaledInstance(jLabel3.getWidth(), jLabel3.getHeight(), Image.SCALE_SMOOTH)));
                break;
            }
        }
        btn_mod.setEnabled(true);
        btn_sup.setEnabled(true);

    }//GEN-LAST:event_jTable1MouseClicked

