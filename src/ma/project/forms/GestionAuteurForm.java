    public String testchooser(String path, boolean choosed) {
        if (choosed) {
            //read image
            try {
                f = new File(path); //image file path
                image = new BufferedImage(width, height, BufferedImage.TYPE_INT_ARGB);
                image = ImageIO.read(f);
                System.out.println("Reading complete.");
            } catch (IOException e) {
                System.out.println("Error: " + e);
            }
            //write image
            try {
                LocalDateTime now = LocalDateTime.now();
                path = "src\\files\\png\\" + jTextField1.getText() + now.getDayOfMonth() + now.getMonthValue() + now.getYear() + ".jpg";
                f = new File(path);  //output file path
                ImageIO.write(image, "jpg", f);

                System.out.println("Writing complete.");
            } catch (IOException e) {
                System.out.println("Error: " + e);
            }
            return path;
        } else {
            path = "";
            return path;
        }

    }

