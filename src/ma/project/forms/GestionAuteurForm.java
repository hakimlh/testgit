    private void addAuteur() {

        aut = gst_aut.ShowData();
        int rowCount = tablemodel.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            tablemodel.removeRow(0);
        }

        for (Auteur au : aut) {
            JLabel lbl = new JLabel();
            if (!au.getPhoto().equalsIgnoreCase("")) {
                lbl.setIcon(new ImageIcon(new ImageIcon(au.getPhoto())
                        .getImage().getScaledInstance(150, 150, Image.SCALE_SMOOTH)));
            } else {
                lbl.setText("No Image View");
            }

            tablemodel.addRow(new Object[]{au.getIdAut(), au.getNom(), au.getDateN(), lbl});
        }
        jTable1.setRowHeight(40);

        jTable1.getColumn("Photo").setCellRenderer(new Labelrenderer());

    }

