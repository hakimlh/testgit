    public void ShowData() {
        adhr = gst_adhr.ShowData();
        int rowCount = tablemodel.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            tablemodel.removeRow(i);
        }

        for (Adherant ad : adhr) {
//            System.err.println(ad.getIdAdr()+" "+ ad.getNumAdherant()+" "+ ad.getNom()+" "+
//                ad.getCin()+" "+ ad.getTele()+" "+ ad.getAdresse()+" "+ad.getEmail());

            tablemodel.addRow(new Object[]{ad.getIdAdr(), ad.getNumAdherant(), ad.getNom(),
                ad.getCin(), ad.getTele(), ad.getAdresse(), ad.getEmail()});
        }
    }

