    private void btn_ajtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ajtActionPerformed
        now = LocalDateTime.now();
        String num = now.getDayOfMonth() + "" + now.getMonthValue() + "" + now.getYear() + "00" + cpt;
        adr = new Adherant(num, nom.getText(), cin.getText(), tele.getText(), adresse.getText(), email.getText());
        gst_adhr.InsertData(adr);
        ShowData();
    }//GEN-LAST:event_btn_ajtActionPerformed

