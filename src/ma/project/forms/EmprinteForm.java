    private void btn_ajtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ajtActionPerformed

        for (Livre lv : arr_livre) {
            if (modelcmb_lvr.getSelectedItem().equals(lv)) {
                idLivre = lv.getIdLivre();
                updateEmp = new Livre(lv.getIdLivre(), lv.getNbrEmp() + 1);
                break;
            }
        }
        for (Adherant ad : arr_adherant) {
            if (modelcmb_adr.getElementAt(cmb_adr.getSelectedIndex()).equals(ad)) {
                idAdherant = ad.getIdAdr();

                break;
            }
        }
        Emprinte em = new Emprinte(idLivre, date_emp.getDate(), date_exp.getDate(), idAdherant);

        gst_emp.InsertData(em);

        gst_lvr.UpdateNombreEmp(updateEmp);

        ShowTableData();
        System.err.println("Fin");

    }//GEN-LAST:event_btn_ajtActionPerformed

    private void btn_modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modActionPerformed
        for (Livre lv : arr_livre) {
            if (modelcmb_lvr.getSelectedItem().equals(lv)) {
                idLivre = lv.getIdLivre();
//                System.out.println(lv.getIdLivre());
                break;
            }
        }
        for (Adherant ad : arr_adherant) {
            if (modelcmb_adr.getElementAt(cmb_adr.getSelectedIndex()).equals(ad)) {
                idAdherant = ad.getIdAdr();
//                System.out.println(ad.getIdAdr());
                break;
            }
        }
        Emprinte em = new Emprinte((int) tablemodel.getValueAt(jTable1.getSelectedRow(), 0), idLivre, date_emp.getDate(), date_exp.getDate(), idAdherant);
        gst_emp.UpdateData(em);
        ShowTableData();
    }//GEN-LAST:event_btn_modActionPerformed

    private void btn_supActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_supActionPerformed
        Emprinte em = new Emprinte((int) tablemodel.getValueAt(jTable1.getSelectedRow(), 0));
        gst_emp.DeleteData(em);
        updateEmp = new Livre(idLivreSelectionne, NbrEmp - 1);
        gst_lvr.UpdateNombreEmp(updateEmp);
        ShowTableData();
    }//GEN-LAST:event_btn_supActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        btn_mod.setEnabled(true);
        btn_sup.setEnabled(true);

        arr_emp = gst_emp.ShowData();

        for (Emprinte em : arr_emp) {
            if (tablemodel.getValueAt(jTable1.getSelectedRow(), 0).equals(em.getIdEmp())) {
                for (Livre livre : arr_livre) {
                    if (livre.getIdLivre() == em.getIdlivreEmp()) {
                        cmb_lvr.setSelectedItem(livre);
                        idLivreSelectionne = livre.getIdLivre();
                        NbrEmp = livre.getNbrEmp();
                        break;
                    }
                }
                for (Adherant ad : arr_adherant) {
                    if (ad.getIdAdr() == em.getIdAdherant()) {
                        cmb_adr.setSelectedItem(ad);
                        break;

                    }
                }
                date_emp.setDate(em.getDateEmp());
                date_exp.setDate(em.getDateExp());

                break;
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

