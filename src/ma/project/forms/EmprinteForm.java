    public void ShowComboData() {
        arr_livre = gst_lvr.ShowData();
        arr_adherant = gst_adr.ShowData();

        modelcmb_lvr.removeAllElements();
        arr_livre.forEach((lv) -> {
            modelcmb_lvr.addElement(lv);
        });

        modelcmb_adr.removeAllElements();
        arr_adherant.forEach((ad) -> {
            modelcmb_adr.addElement(ad);
        });

    }

    public void ShowTableData() {

        arr_emp = gst_emp.ShowData();

        int rowCount = tablemodel.getRowCount();

        for (int i = 0; i < rowCount; i++) {
            tablemodel.removeRow(0);
        }
        for (Emprinte em : arr_emp) {
            tablemodel.addRow(new Object[]{em.getIdEmp(), em.getIdlivreEmp(),
                em.getIdAdherant(), em.getDateEmp(), em.getDateExp()});
        }

    }

