    private void btn_supActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_supActionPerformed
        fctn = new Fonctionnaire((int) tablemodel.getValueAt(jTable1.getSelectedRow(), 0));
        gst_fonctionnaire.DeleteData(fctn);
        ShowData();
    }//GEN-LAST:event_btn_supActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        arr_fctn = gst_fonctionnaire.ShowData();
        for (Fonctionnaire o : arr_fctn) {
            if (tablemodel.getValueAt(jTable1.getSelectedRow(), 0).equals(o.getIdFonctionnaire())) {
                jTextField1.setText(o.getNom());
                jTextField2.setText(o.getCin());
                jTextField3.setText(o.getAdresse());
                jTextField4.setText(o.getTele());
                jTextField5.setText(o.getEmail());
                for (Fonction f : arr_fct) {
                    if (o.getFonction().getFonction().equals(f.getFonction())) {
                        jComboBox1.setSelectedItem(f);
                        fct = f;
                        break;
                    }
                }
                break;

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btn_modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modActionPerformed
        fctn = new Fonctionnaire((int) tablemodel.getValueAt(jTable1.getSelectedRow(), 0), jTextField1.getText(),
                jTextField2.getText(), jTextField3.getText(), jTextField4.getText(), jTextField5.getText(), fct);
        gst_fonctionnaire.UpdateData(fctn);
        ShowData();
    }//GEN-LAST:event_btn_modActionPerformed

    private void btn_ajtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ajtActionPerformed
        for (Fonction f : arr_fct) {
            if (modelcmb_fct.getSelectedItem().equals(f)) {
                fct = f;
                break;
            }
        }
        fctn = new Fonctionnaire(jTextField1.getText(),
                jTextField2.getText(), jTextField3.getText(), jTextField4.getText(),
                 jTextField5.getText(), fct);
        gst_fonctionnaire.InsertData(fctn);
        ShowData();

    }//GEN-LAST:event_btn_ajtActionPerformed

