    public void ShowData() {
        arr_fctn = gst_fonctionnaire.ShowData();
        arr_fct = gst_fonction.ShowData();

        int rowCount = tablemodel.getRowCount();

        modelcmb_fct.removeAllElements();
        arr_fct.forEach((f) -> {
            modelcmb_fct.addElement(f);
        });

        for (int i = 0; i < rowCount; i++) {
            tablemodel.removeRow(i);
        }

        for (Fonctionnaire o : arr_fctn) {

            tablemodel.addRow(new Object[]{o.getIdFonctionnaire(), o.getNom(), o.getCin(), o.getAdresse(), o.getTele(),
                o.getEmail(), o.getFonction().toString()});
        }
    }

